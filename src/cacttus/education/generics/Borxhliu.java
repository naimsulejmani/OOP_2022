package cacttus.education.generics;

public class Borxhliu {
    private String emri;
    private double vlera;

    public Borxhliu(String emri, double vlera) {
        this.emri = emri;
        this.vlera = vlera;
    }

    public String getEmri() {
        return emri;
    }

    public void setEmri(String emri) {
        this.emri = emri;
    }

    public double getVlera() {
        return vlera;
    }

    public void setVlera(double vlera) {
        this.vlera = vlera;
    }
}
